    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
        
        double a;
        double b;
        int ticketCount = Integer.parseInt(jTextField1.getText()) + Integer.parseInt(jTextField3.getText());
        
        if(jTextField1.getText().equals("") || jTextField3.getText().equals("")){
            System.out.println("Enter valid numbers.");
        }else {
            
            a = Double.parseDouble(jTextField1.getText());
            b = Double.parseDouble(jTextField3.getText());
            
            
            if(jComboBox2.getItemAt(jComboBox2.getSelectedIndex()).equals("VIP")){
                a *= 1.8;
                b *= 2;
            }else if(jComboBox2.getItemAt(jComboBox2.getSelectedIndex()).equals("Class A")){
                a *= 1.4;
                b *= 1.2;
            }
            
            //VIP tickets are sold for 2 times the normal ticket price (student = 1.8)
            //Class A tickets are sold for 1.4 times the normal ticket price (student = 1.2)
            //Class B tickets are sold for normal price which is 15 dollars
            //These values can be changed and itared based on the concerts ticket sellling preferences
            
            
            
            
            double totalTicket = a + b ;
            if(true){
                jLabelAvailable.setVisible(true);
                jLabelAvailable1.setVisible(false);
            }else if(false){
                jLabelAvailable1.setVisible(true);
                jLabelAvailable.setVisible(false);
            }
            String x = Double.toString((Math.round((totalTicket*15)*100)/100.0));
            jLabel9.setText(x+" $");
            
            //set model to JList
            lstData.setModel(mod);
            for (int i = 0; i < ticketCount; i++) {
                mod.addElement("Ticket " + (i+1)); 
            }
           
           
        }
    }//GEN-LAST:event_jButton1ActionPerformed

